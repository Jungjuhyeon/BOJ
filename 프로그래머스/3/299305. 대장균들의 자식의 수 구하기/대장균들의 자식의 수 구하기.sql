SELECT a.ID,
       IFNULL(sub.c,0) as CHILD_COUNT
FROM ECOLI_DATA as a
LEFT JOIN (
  SELECT PARENT_ID,
          COUNT(*) as c
  FROM ECOLI_DATA
  WHERE PARENT_ID IS NOT NULL
  GROUP BY PARENT_ID
) as sub
ON a.ID = sub.PARENT_ID
ORDER BY 1 
