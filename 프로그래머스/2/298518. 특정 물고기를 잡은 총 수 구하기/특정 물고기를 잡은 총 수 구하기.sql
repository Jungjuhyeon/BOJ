SELECT count(*) AS FISH_COUNT
  FROM FISH_INFO as info
  JOIN FISH_NAME_INFO as name
    ON info.FISH_TYPE = name.FISH_TYPE
 WHERE name.FISH_NAME IN ('BASS', 'SNAPPER') 
















