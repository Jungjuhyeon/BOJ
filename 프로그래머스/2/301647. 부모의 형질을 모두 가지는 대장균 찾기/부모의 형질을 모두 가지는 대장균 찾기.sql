SELECT b.ID,
       b.GENOTYPE,
       a.GENOTYPE as PARENT_GENOTYPE
FROM ECOLI_DATA as a
JOIN ECOLI_DATA as b
  ON a.ID = b.PARENT_ID
WHERE a.GENOTYPE & b.GENOTYPE >= a.GENOTYPE
ORDER BY ID















